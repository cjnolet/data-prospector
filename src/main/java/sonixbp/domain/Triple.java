package sonixbp.domain;


public class Triple {

    String subject;
    String predicate;
    String object;

    String type;
    String visibility;

    public String getSubject() {
        return subject;
    }

    public String getPredicate() {
        return predicate;
    }

    public String getObject() {
        return object;
    }

    public String getType() {
        return type;
    }

    public String getVisibility() {
        return visibility;
    }
}
